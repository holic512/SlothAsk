package org.example.servicegateway.config.exception;

public class CustomNotLoginException extends RuntimeException {
    private int code;
    private String message;

    public CustomNotLoginException(int code, String message) {
        super(message);
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    @Override
    public String getMessage() {
        return message;
    }
}
